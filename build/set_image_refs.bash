#!/bin/bash

# This script sets the image ref environment variable for all the container image. For that, it uses a environment variables DOCKER_ACCOUNT and BUILD_TAG_NAME.
# Example usage: source set_image_refs.bash

DLTK_SPARK_RUNTIME_DRIVER_IMAGE_BASE=$DOCKER_ACCOUNT/spark-runtime-driver
DLTK_SPARK_RUNTIME_EXECUTOR_IMAGE_BASE=$DOCKER_ACCOUNT/spark-runtime-executor
DLTK_SPARK_RUNTIME_DRIVER_PROXY_IMAGE_BASE=$DOCKER_ACCOUNT/spark-runtime-driver-proxy
DLTK_SPARK_RUNTIME_EDITOR_IMAGE_BASE=$DOCKER_ACCOUNT/spark-runtime-editor
DLTK_SPARK_RUNTIME_INBOUND_RELAY_IMAGE_BASE=$DOCKER_ACCOUNT/spark-runtime-inbound-relay
DLTK_SPARK_RUNTIME_OUTBOUND_RELAY_IMAGE_BASE=$DOCKER_ACCOUNT/spark-runtime-outbound-relay
DLTK_SPARK_RUNTIME_DRIVER_IMAGE=$DLTK_SPARK_RUNTIME_DRIVER_IMAGE_BASE:$BUILD_TAG_NAME
DLTK_SPARK_RUNTIME_EXECUTOR_IMAGE=$DLTK_SPARK_RUNTIME_EXECUTOR_IMAGE_BASE:$BUILD_TAG_NAME
DLTK_SPARK_RUNTIME_DRIVER_PROXY_IMAGE=$DLTK_SPARK_RUNTIME_DRIVER_PROXY_IMAGE_BASE:$BUILD_TAG_NAME
DLTK_SPARK_RUNTIME_EDITOR_IMAGE=$DLTK_SPARK_RUNTIME_EDITOR_IMAGE_BASE:$BUILD_TAG_NAME
DLTK_SPARK_RUNTIME_INBOUND_RELAY_IMAGE=$DLTK_SPARK_RUNTIME_INBOUND_RELAY_IMAGE_BASE:$BUILD_TAG_NAME
DLTK_SPARK_RUNTIME_OUTBOUND_RELAY_IMAGE=$DLTK_SPARK_RUNTIME_OUTBOUND_RELAY_IMAGE_BASE:$BUILD_TAG_NAME
DLTK_BASE_RUNTIME_IMAGE_BASE=$DOCKER_ACCOUNT/base-runtime
DLTK_BASE_RUNTIME_IMAGE=$DLTK_BASE_RUNTIME_IMAGE_BASE:$BUILD_TAG_NAME